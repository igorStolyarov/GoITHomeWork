package com.goit.gojavaonlinethree.student.igorstolyarov.module04;

class Shapes {
    public static void main(String[] args) {
        // Triangel

        float triangelAreaA = 5f;
        float triangelAreaH = 3f;

        System.out.println("Area of a triangle = " + (triangelAreaA * triangelAreaH) / 2);

        // Rectangel

        int rectAngelA = 7;
        int rectAngelB = 8;

        System.out.println("Area of a rectangle = " + rectAngelA * rectAngelB);

        // Circle

        double radius = 5d;

        System.out.println("Area of a circle = " + Math.PI * (radius * radius));

    }
}
