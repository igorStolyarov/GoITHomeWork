package com.goit.gojavaonlinethree.student.igorstolyarov.module04;

class Distance {
    public static void main(String[] args) {

        double x1 = 1;
        double y1 = 1;
        double x2 = 2;
        double y2 = 2;
        double distancesX = x2 - x1;
        double distancesY = y2 - y1;

        System.out.println("Distance between points = " + ((distancesX) + (distancesY)) / 2);
    }
}
